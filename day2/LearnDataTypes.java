package week1.day2;

public class LearnDataTypes {

	public static void main(String[] args) {
		
		//Integer - Numbers
		byte noOfVehicle = 2;
		short age = 29;
		int price = 17000; //Most used data type
        long cardNumber = 12345678996891828l; //To use long data type, it should be ended with l or L
	System.out.println("No of vehicle : "+noOfVehicle);
	System.out.println("Age : "+age);
	System.out.println("Price : " +price);
	System.out.println("Card Number : "+cardNumber);
	
	//Float - Decimal
	float petrolPrice = 108.76f; //To use float data type, it should be ended with f or F
	double pieValue = 3.14666667;
	System.out.println("Petrol Price : " +petrolPrice);
	System.out.println("Pie Value : " +pieValue);
	
	//Character
	char logo = '1'; //Single value inside single quotes or ASCII value without quotes
	System.out.println("Logo : " +logo);
	
	//Boolean - Yes or No - True or false in lower case
	boolean haveYouHadYourBreakfast = true;
	System.out.println("Have you had your breakfast : " +haveYouHadYourBreakfast);
	
	//String
	String name = "Sangeetha";
	System.out.println("My name is : " +name);
	
	
	}

}
